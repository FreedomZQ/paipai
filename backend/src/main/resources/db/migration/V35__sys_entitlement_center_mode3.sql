-- 统一后台式权益中心（模式三：老用户权益保护版）。
-- 中文说明：统一后端同时承载拍拍伴读、saving、FitMystery 等多个 App，权益配置必须按 app_code 隔离。
-- 设计目标：
-- 1) 免费权益按当前后台配置动态调整；
-- 2) 付费权益在有效期内保留购买时快照，后台降配不能影响已付款承诺；
-- 3) 新增/提升权益自动给有效期内老用户补齐；
-- 4) 人工赠送和用户级覆盖不要写 sys_entitlement_snapshot，避免被 App Store 刷新覆盖。

CREATE TABLE IF NOT EXISTS public.sys_membership_plan (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    app_code varchar(64) NOT NULL,
    plan_code varchar(64) NOT NULL,
    display_name varchar(128) NOT NULL,
    description varchar(512),
    plan_level integer DEFAULT 0 NOT NULL,
    entitlement_code varchar(64),
    status varchar(32) DEFAULT 'active' NOT NULL,
    sort_order integer DEFAULT 0 NOT NULL,
    metadata_json jsonb,
    created_at timestamp with time zone DEFAULT CURRENT_TIMESTAMP NOT NULL,
    updated_at timestamp with time zone DEFAULT CURRENT_TIMESTAMP NOT NULL
);
CREATE UNIQUE INDEX IF NOT EXISTS uk_sys_membership_plan_app_plan ON public.sys_membership_plan(app_code, plan_code);
CREATE INDEX IF NOT EXISTS idx_sys_membership_plan_app_status ON public.sys_membership_plan(app_code, status);

