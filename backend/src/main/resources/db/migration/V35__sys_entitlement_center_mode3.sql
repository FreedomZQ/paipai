-- 老用户补购买时快照：只补仍 active 且未过期的付费权益；过期记录不参与权益保护。
INSERT INTO public.sys_user_plan_snapshot(app_code, user_id, plan_code, entitlement_code, policy_version, source_type, source_ref, status, starts_at, expires_at, feature_snapshot_json, created_at, updated_at)
SELECT
    s.app_code,
    s.user_id,
    p.plan_code,
    s.entitlement_code,
    s.app_code || ':' || p.plan_code || ':mode3_backfill_20260428',
    'legacy_backfill',
    'sys_entitlement_snapshot:' || s.id,
    'active',
    COALESCE(s.created_at, now()),
    s.expires_at,
    jsonb_build_object(
        'version', s.app_code || ':' || p.plan_code || ':mode3_backfill_20260428',
        'planCode', p.plan_code,
        'entitlementCode', s.entitlement_code,
        'features', COALESCE((
            SELECT jsonb_object_agg(r.feature_code, jsonb_build_object(
                'enabled', r.enabled,
                'accessLevel', r.access_level,
                'limitValue', r.limit_value,
                'limitUnit', r.limit_unit,
                'scopeCode', r.scope_code,
                'source', 'legacy_backfill_current_rule'
            ))
            FROM public.sys_plan_feature_rule r
            WHERE r.app_code = p.app_code AND r.plan_code = p.plan_code AND r.status = 'active'
        ), '{}'::jsonb)
    ),
    now(),
    now()
FROM public.sys_entitlement_snapshot s
JOIN public.sys_membership_plan p
  ON p.app_code = s.app_code
 AND p.entitlement_code = s.entitlement_code
 AND p.status = 'active'
WHERE s.status = 'active'
  AND COALESCE(s.entitlement_code, '') <> 'free'
  AND (s.expires_at IS NULL OR s.expires_at > now())
  AND NOT EXISTS (
      SELECT 1 FROM public.sys_user_plan_snapshot existing
      WHERE existing.app_code = s.app_code
        AND existing.user_id = s.user_id
        AND existing.source_ref = 'sys_entitlement_snapshot:' || s.id
  );
