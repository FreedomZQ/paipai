CREATE TABLE IF NOT EXISTS public.reading_resource_pack_catalog (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    app_code character varying(64) DEFAULT 'paipai_readingcompanion'::character varying NOT NULL,
    package_code character varying(128) NOT NULL,
    package_type character varying(64) NOT NULL,
    service_type character varying(64) NOT NULL,
    display_name_json jsonb DEFAULT '{}'::jsonb NOT NULL,
    display_description_json jsonb DEFAULT '{}'::jsonb NOT NULL,
    price_amount_cents integer NOT NULL,
    currency_code character varying(8) DEFAULT 'CNY'::character varying NOT NULL,
    included_quantity integer NOT NULL,
    quantity_unit character varying(32) NOT NULL,
    valid_days integer NOT NULL,
    status character varying(32) DEFAULT 'active'::character varying NOT NULL,
    sort_order integer DEFAULT 100 NOT NULL,
    metadata_json jsonb DEFAULT '{}'::jsonb NOT NULL,
    created_at timestamp with time zone DEFAULT CURRENT_TIMESTAMP NOT NULL,
    updated_at timestamp with time zone DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT ck_reading_resource_pack_catalog_quantity CHECK (included_quantity > 0),
    CONSTRAINT ck_reading_resource_pack_catalog_price CHECK (price_amount_cents >= 0),
    CONSTRAINT ck_reading_resource_pack_catalog_valid_days CHECK (valid_days > 0)
);

CREATE UNIQUE INDEX IF NOT EXISTS uk_reading_resource_pack_catalog_app_package
    ON public.reading_resource_pack_catalog(app_code, package_code);

CREATE INDEX IF NOT EXISTS idx_reading_resource_pack_catalog_status
    ON public.reading_resource_pack_catalog(app_code, status, package_type, sort_order);

